package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Students\nToday we have Java Class"); // This sequence adds a new line

        System.out.println("------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\t\tJava Developer Course Batch 1");
        System.out.println("_______________________________");
        System.out.println("\\");
        System.out.println("________________________________");
        System.out.println("My Favorite movie is \"Friends\"");


    }




}
/*
1.\n: New line
2.\t:Paragraph space
3.\\:Single back slash
4.\"Double quote

 */

